#!/usr/bin/env node
// ─────────────────────────────────────────────────────────────────────
//  Andromeda dataset seller
//
//  Sells one product: NOAA Pacific-Northwest weather archive 2015-2025.
//  Endpoints:
//    GET  /api/health
//    GET  /api/v1/discovery
//    GET  /api/v1/dataset/:id/preview              (free, 100-row preview)
//    POST /api/v1/dataset/:id/purchase             (L402-paywalled, 5000 sat)
//    GET  /api/v1/dataset/:id/download?signed_url= (signed-URL, 24h)
//
//  In mock mode, the "full" download is the same fixture file; in real
//  mode, ANDROMEDA_DATASET_FILE points at the actual Parquet.
// ─────────────────────────────────────────────────────────────────────

import http from "node:http";
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { createHmac, createHash, randomBytes } from "node:crypto";
import { generateKeypair, pubkeyFor, signRequest, mintMacaroon, verifyMacaroon, verifyPreimage, parseAuthHeader } from "@andromeda/core";

const HERE = path.dirname(fileURLToPath(import.meta.url));

function loadDotEnv() {
  const fp = path.resolve(HERE, "..", ".env");
  if (!fs.existsSync(fp)) return;
  for (const line of fs.readFileSync(fp, "utf8").split("\n")) {
    const m = line.match(/^([A-Z_][A-Z0-9_]*)=(.*)$/);
    if (m && !process.env[m[1]]) process.env[m[1]] = m[2];
  }
}
loadDotEnv();

const PORT = parseInt(process.env.PORT ?? "3200", 10);
const REGISTRY_URL = process.env.ANDROMEDA_REGISTRY_URL ?? "http://localhost:3030";
const MOCK_MODE = process.env.MOCK_MODE !== "false";
const PUBLIC_URL = process.env.ANDROMEDA_PROVIDER_PUBLIC_URL ?? `http://localhost:${PORT}`;
const L402_SECRET = process.env.L402_SECRET ?? "";
const PLATFORM_FEE_BPS = parseInt(process.env.ANDROMEDA_PLATFORM_FEE_BPS ?? "200", 10);
if (L402_SECRET.length < 32) {
  console.warn("[dataset-seller] L402_SECRET <32 chars; signed-URLs will be weak");
}

const PRICE_SATS = 5000;

// ─── identity ────────────────────────────────────────────────────────
let identity = null;
async function ensureIdentity() {
  if (identity) return identity;
  let priv = process.env.ANDROMEDA_PROVIDER_PRIVKEY;
  if (!priv) {
    const kp = await generateKeypair();
    priv = kp.privkey_hex;
    const fp = path.resolve(HERE, "..", ".env");
    const line = `\n# auto-generated by dataset-seller on first boot\nANDROMEDA_PROVIDER_PRIVKEY=${priv}\n`;
    try {
      if (fs.existsSync(fp)) fs.appendFileSync(fp, line, "utf8");
      else fs.writeFileSync(fp, line, "utf8");
      console.error(`[dataset-seller] generated keypair; pubkey=${kp.pubkey_hex}`);
    } catch (e) {
      console.error(`[dataset-seller] WARN ${e.message}`);
    }
    process.env.ANDROMEDA_PROVIDER_PRIVKEY = priv;
    identity = { privkey: priv, pubkey: kp.pubkey_hex };
    return identity;
  }
  identity = { privkey: priv, pubkey: await pubkeyFor(priv) };
  return identity;
}

// ─── data file ───────────────────────────────────────────────────────
const FIXTURE = path.resolve(HERE, "fixtures", "noaa-pnw.json");
function loadDataset() { return JSON.parse(fs.readFileSync(FIXTURE, "utf8")); }

// ─── invoice store (in-memory; we don't persist for simplicity) ──────
// Keyed by payment_hash → { preimage, amount, expires_at }
const invoices = new Map();
function makeInvoice(amount, ttlSec) {
  const preimage = randomBytes(32).toString("hex");
  const payment_hash = createHash("sha256").update(Buffer.from(preimage, "hex")).digest("hex");
  const expires_at = Math.floor(Date.now() / 1000) + ttlSec;
  invoices.set(payment_hash, { preimage, amount, expires_at });
  return {
    invoice: `lnbcMOCK${amount}u1${payment_hash.slice(0, 40)}`,
    payment_hash, amount, expires_at,
  };
}

// ─── platform-fee tx record (fire & forget) ──────────────────────────
async function recordTx(args) {
  const id = identity;
  if (!id) return;
  const platformFee = Math.round(args.amount_sats * PLATFORM_FEE_BPS / 10000);
  const body = JSON.stringify({
    id: `tx_${args.payment_hash.slice(0, 24)}`,
    buyer_pubkey: args.buyer_pubkey ?? "unknown",
    seller_pubkey: id.pubkey,
    service_id: `${id.pubkey.slice(0, 8)}:${args.service_local_id}`,
    amount_sats: args.amount_sats,
    platform_fee_sats: platformFee,
    payment_hash: args.payment_hash,
    settled_at: Date.now(),
  });
  try {
    const headers = await signRequest({
      method: "POST", path: "/api/v1/transactions/record", body,
      privkeyHex: id.privkey, pubkeyHex: id.pubkey,
    });
    await fetch(`${REGISTRY_URL}/api/v1/transactions/record`, {
      method: "POST",
      headers: { "content-type": "application/json", ...headers },
      body,
    });
  } catch (e) {
    console.error(`[dataset-seller] tx record failed: ${e.message}`);
  }
}

// ─── signed download URL ─────────────────────────────────────────────
function signDownloadUrl(dataset_id, ttlSec) {
  const exp = Math.floor(Date.now() / 1000) + ttlSec;
  const nonce = randomBytes(8).toString("hex");
  const payload = `${dataset_id}|${exp}|${nonce}`;
  const sig = createHmac("sha256", L402_SECRET || "fallback").update(payload).digest("base64url");
  return `${PUBLIC_URL}/api/v1/dataset/${dataset_id}/download?exp=${exp}&nonce=${nonce}&sig=${sig}`;
}
function verifyDownloadSig(dataset_id, exp, nonce, sig) {
  if (!exp || !nonce || !sig) return false;
  if (parseInt(exp, 10) < Math.floor(Date.now() / 1000)) return false;
  const payload = `${dataset_id}|${exp}|${nonce}`;
  const expected = createHmac("sha256", L402_SECRET || "fallback").update(payload).digest("base64url");
  return sig === expected;
}

// ─── http helpers ────────────────────────────────────────────────────
function send(res, status, body, extraHeaders = {}) {
  const text = typeof body === "string" ? body : JSON.stringify(body);
  res.writeHead(status, { "content-type": "application/json", "content-length": Buffer.byteLength(text), ...extraHeaders });
  res.end(text);
}
async function readJson(req) {
  return new Promise((resolve) => {
    let data = "";
    req.on("data", c => { data += c; });
    req.on("end", () => {
      if (!data) return resolve(null);
      try { resolve(JSON.parse(data)); } catch { resolve(null); }
    });
  });
}

// ─── server ──────────────────────────────────────────────────────────
const server = http.createServer(async (req, res) => {
  try {
    const url = new URL(req.url, `http://localhost:${PORT}`);
    const p = url.pathname;
    const m = req.method;

    if (m === "GET" && p === "/api/health") {
      return send(res, 200, {
        ok: true, service: "andromeda-dataset-seller", rev: "v0.1.0",
        andromeda_pubkey: identity?.pubkey ?? null,
        wallet_mode: MOCK_MODE ? "mock" : "real",
      });
    }

    if (m === "GET" && p === "/api/v1/discovery") {
      const ds = loadDataset();
      return send(res, 200, {
        schema: "andromeda.directory.v1",
        provider: { id: "dataset-seller", pubkey: identity?.pubkey, url: PUBLIC_URL },
        services: [{
          id: ds.dataset_id,
          endpoint: `${PUBLIC_URL}/api/v1/dataset/${ds.dataset_id}/purchase`,
          method: "POST",
          category: "data.archive",
          type: "dataset",
          description: ds.description,
          price_sats: PRICE_SATS,
          provenance: { source: ds.source, license: ds.license },
          last_updated: "2026-04-26",
          row_count: ds.row_count,
          size_bytes: ds.size_bytes,
          preview_endpoint: `${PUBLIC_URL}/api/v1/dataset/${ds.dataset_id}/preview`,
        }],
      });
    }

    let m2;
    if (m === "GET" && (m2 = p.match(/^\/api\/v1\/dataset\/([^\/]+)\/preview$/))) {
      const ds = loadDataset();
      if (m2[1] !== ds.dataset_id) return send(res, 404, { error: "no such dataset" });
      // Return up to 100 rows (the fixture only has 10; we'd repeat in real impl).
      return send(res, 200, {
        dataset_id: ds.dataset_id,
        name: ds.name,
        preview_rows: ds.preview_rows.slice(0, 100),
        full_row_count: ds.row_count,
        full_size_bytes: ds.size_bytes,
        purchase_endpoint: `${PUBLIC_URL}/api/v1/dataset/${ds.dataset_id}/purchase`,
        price_sats: PRICE_SATS,
      });
    }

    if (m === "POST" && (m2 = p.match(/^\/api\/v1\/dataset\/([^\/]+)\/purchase$/))) {
      const ds = loadDataset();
      if (m2[1] !== ds.dataset_id) return send(res, 404, { error: "no such dataset" });
      const RESOURCE = `/v1/dataset/${ds.dataset_id}/purchase`;

      const auth = req.headers.authorization ?? null;
      if (!auth) {
        // Issue 402 challenge.
        const inv = makeInvoice(PRICE_SATS, 300);
        const macaroon = mintMacaroon({
          payment_hash: inv.payment_hash,
          resource: RESOURCE,
          amount: PRICE_SATS,
          exp: inv.expires_at,
        }, L402_SECRET);
        const body = {
          error: "payment_required",
          message: `${PRICE_SATS} sats required for ${ds.dataset_id}`,
          invoice: inv.invoice,
          payment_hash: inv.payment_hash,
          amount_sats: PRICE_SATS,
          expires_at: inv.expires_at,
          macaroon,
        };
        return send(res, 402, body, {
          "www-authenticate": `L402 macaroon="${macaroon}", invoice="${inv.invoice}"`,
        });
      }

      const parsed = parseAuthHeader(auth);
      if (!parsed) return send(res, 401, { error: "malformed L402 header" });
      const macBody = verifyMacaroon(parsed.macaroon, L402_SECRET);
      if (!macBody) return send(res, 401, { error: "invalid macaroon" });
      if (macBody.resource !== RESOURCE) return send(res, 401, { error: "resource mismatch" });
      if (!verifyPreimage(parsed.preimage, macBody.payment_hash)) {
        return send(res, 401, { error: "preimage mismatch" });
      }
      // Idempotent — drop from invoice store.
      invoices.delete(macBody.payment_hash);

      // Issue signed download URL.
      const buyerPubkey = req.headers["x-andromeda-pubkey"] ?? null;
      const downloadUrl = signDownloadUrl(ds.dataset_id, 24 * 3600);

      // Record platform fee.
      void recordTx({
        buyer_pubkey: buyerPubkey,
        service_local_id: ds.dataset_id,
        amount_sats: macBody.amount,
        payment_hash: macBody.payment_hash,
      });

      return send(res, 200, {
        ok: true,
        dataset_id: ds.dataset_id,
        signed_url: downloadUrl,
        valid_for_seconds: 24 * 3600,
        amount_sats_paid: macBody.amount,
        platform_fee_sats: Math.round(macBody.amount * PLATFORM_FEE_BPS / 10000),
      });
    }

    if (m === "POST" && p === "/api/dev/pay") {
      if (!MOCK_MODE) return send(res, 404, { error: "dev endpoint disabled" });
      const body = await readJson(req);
      if (!body?.payment_hash) return send(res, 400, { error: "payment_hash required" });
      const inv = invoices.get(body.payment_hash);
      if (!inv) return send(res, 404, { error: "no such invoice" });
      return send(res, 200, { preimage: inv.preimage, paid: true });
    }

    if (m === "GET" && (m2 = p.match(/^\/api\/v1\/dataset\/([^\/]+)\/download$/))) {
      const ds = loadDataset();
      if (m2[1] !== ds.dataset_id) return send(res, 404, { error: "no such dataset" });
      const exp = url.searchParams.get("exp");
      const nonce = url.searchParams.get("nonce");
      const sig = url.searchParams.get("sig");
      if (!verifyDownloadSig(ds.dataset_id, exp, nonce, sig)) {
        return send(res, 403, { error: "signed URL invalid or expired" });
      }
      // Return the JSON fixture in mock mode (treated as the dataset).
      // In real mode this would stream the actual Parquet file.
      const text = fs.readFileSync(FIXTURE, "utf8");
      res.writeHead(200, {
        "content-type": "application/json",
        "content-length": Buffer.byteLength(text),
        "x-andromeda-dataset-id": ds.dataset_id,
        "content-disposition": `attachment; filename="${ds.dataset_id}.json"`,
      });
      res.end(text);
      return;
    }

    send(res, 404, { error: "not found", path: p, method: m });
  } catch (e) {
    console.error("[dataset-seller] error:", e.stack || e.message);
    send(res, 500, { error: "internal", message: e.message });
  }
});

// ─── self-register ───────────────────────────────────────────────────
async function registerWithRegistry() {
  const id = await ensureIdentity();
  const ds = loadDataset();
  const path = "/api/v1/sellers/register";
  const body = JSON.stringify({
    pubkey: id.pubkey,
    name: "dataset-seller",
    url: PUBLIC_URL,
    description: "Sells curated data products with signed-URL delivery (24h validity).",
    services: [{
      local_id: ds.dataset_id,
      name: ds.name,
      description: `${ds.description} Provenance: ${ds.source}, license: ${ds.license}.`,
      type: "dataset",
      tags: ["data", "noaa", "weather", "pacific-northwest", "archive", "parquet"],
      price_sats: PRICE_SATS,
      p50_ms: 800,
      endpoint: `${PUBLIC_URL}/api/v1/dataset/${ds.dataset_id}/purchase`,
    }],
  });
  try {
    const headers = await signRequest({ method: "POST", path, body, privkeyHex: id.privkey, pubkeyHex: id.pubkey });
    const r = await fetch(`${REGISTRY_URL}${path}`, {
      method: "POST",
      headers: { "content-type": "application/json", ...headers },
      body,
    });
    if (!r.ok) console.error(`[dataset-seller] register failed: ${r.status} ${(await r.text()).slice(0, 200)}`);
    else console.error(`[dataset-seller] registered with ${REGISTRY_URL}; pubkey=${id.pubkey}`);
  } catch (e) {
    console.error(`[dataset-seller] register error: ${e.message}`);
  }
}

async function boot() {
  await ensureIdentity();
  server.listen(PORT, () => {
    console.error(`[dataset-seller] listening on :${PORT} (${MOCK_MODE ? "mock" : "real"} mode)`);
  });
  await registerWithRegistry();
  setInterval(() => { void registerWithRegistry().catch(() => {}); }, 60_000);
}

process.on("SIGINT", () => { server.close(); process.exit(0); });
process.on("SIGTERM", () => { server.close(); process.exit(0); });

boot().catch(e => {
  console.error("[dataset-seller] fatal:", e.stack || e.message);
  process.exit(1);
});
